// Mission-aware prompt templates for each phase of the mission lifecycle.
//
// These templates inject mission context (criteria, gaps, research, handoffs)
// into the prompts that agents receive. They ensure agents understand:
//   - What the mission is trying to accomplish (intent)
//   - What acceptance criteria must be satisfied
//   - What gaps remain from prior validation
//   - What previous agents accomplished (handoff history)
//   - What research has been gathered
//
// Each phase prompt follows the self-reflective loop pattern established
// in the existing Stoke prompts: think → research → verify → act.
package prompts

import (
	"fmt"
	"strings"
)

// MissionContext is the structured context injected into mission-aware prompts.
// Generated by the mission ContextBuilder.
type MissionContext struct {
	MissionID      string   // mission identifier
	Title          string   // short mission name
	Intent         string   // full user intent
	Phase          string   // current lifecycle phase
	CriteriaBlock  string   // formatted criteria checklist
	GapsBlock      string   // formatted open gaps
	ResearchBlock  string   // formatted research findings
	HandoffBlock   string   // formatted handoff history
	StatusBlock    string   // convergence status summary
	PriorContext   string   // any additional context from prior executions

	// Project type information for conditional prompt sections
	HasFrontend   bool   // true if project includes UI components
	UIFramework   string // e.g. "react", "vue", "svelte", "angular"
	TestFramework string // e.g. "jest", "vitest", "playwright"
	HasStorybook  bool   // true if Storybook detected
}

// BuildMissionResearchPrompt generates the prompt for the Researching phase.
// The agent must gather information needed to plan the implementation.
func BuildMissionResearchPrompt(ctx MissionContext) string {
	var b strings.Builder

	fmt.Fprintf(&b, `You are a research agent working on mission "%s".

## Mission
**Title:** %s
**Intent:** %s

`, ctx.MissionID, ctx.Title, ctx.Intent)

	if ctx.CriteriaBlock != "" {
		fmt.Fprintf(&b, "%s\n", ctx.CriteriaBlock)
	}

	b.WriteString(`## Your Task: Research Phase

Gather all information needed to plan and implement this mission.

### CRITICAL: Research Over Recall
Your training data is a potentially STALE CACHE, not a source of truth.
- EVERY API usage MUST be confirmed against CURRENT documentation, not memory.
- EVERY library behavior MUST be confirmed against source or tests.
- EVERY configuration format MUST be validated against the tool's actual schema.
- EVERY dependency version MUST be checked for latest stable, known CVEs, and deprecation.
- If you cannot verify a fact against an authoritative source, mark it as UNVERIFIED.

### Step 1: Analyze the intent
Break down the user's intent into specific technical requirements. For each:
- What needs to exist that doesn't today?
- What existing code needs to change?
- What interfaces, contracts, or APIs are affected?
- What surfaces need this? (API, Web, Mobile, Desktop, CLI, MCP)
- What security boundaries apply? (auth, RBAC, input validation)

### Step 2: Research the codebase (deep, not shallow)
Do NOT just grep for keywords. Trace actual code paths:
- Existing patterns and conventions (read real implementations, not just names)
- Consumer/producer relationships (who calls what, who depends on what)
- Test infrastructure and coverage gaps
- Build, deploy, and CI/CD constraints
- Adjacent incomplete work (scaffolds, stubs, TODOs anywhere in the repo)

### Step 3: Research external knowledge
For EVERY external dependency or API:
- Fetch and read the CURRENT documentation (do not rely on training data)
- Confirm version compatibility and deprecation status
- Identify security implications and common CVEs
- Find prior art in production-grade systems (not toy examples)

### Step 4: Completeness check
Ask: "Do I have enough VERIFIED information to write a detailed, accurate plan?"
For each fact in your research, can you cite where you verified it?
If NO: identify gaps and continue researching.
If YES: produce your research summary with source citations.

### Output
Produce a structured research document with:
- **Requirements breakdown**: numbered list of specific technical requirements
- **Codebase findings**: relevant files, patterns, and constraints (with file paths)
- **External research**: domain knowledge with source citations
- **Verified facts**: each with source (URL, file path, or doc reference)
- **Unverified assumptions**: anything you could not confirm (be honest)
- **Adjacent issues**: broken or incomplete code discovered during research
- **Recommended approach**: high-level strategy based on verified research

`)

	if ctx.ResearchBlock != "" {
		fmt.Fprintf(&b, "## Prior Research\n%s\n", ctx.ResearchBlock)
	}
	if ctx.HandoffBlock != "" {
		fmt.Fprintf(&b, "## Handoff Context\n%s\n", ctx.HandoffBlock)
	}

	return b.String()
}

// BuildMissionPlanPrompt generates the prompt for the Planning phase.
// The agent must create a detailed implementation plan with verification criteria.
func BuildMissionPlanPrompt(ctx MissionContext) string {
	var b strings.Builder

	fmt.Fprintf(&b, `You are a planning agent working on mission "%s".

## Mission
**Title:** %s
**Intent:** %s

`, ctx.MissionID, ctx.Title, ctx.Intent)

	if ctx.CriteriaBlock != "" {
		fmt.Fprintf(&b, "%s\n", ctx.CriteriaBlock)
	}
	if ctx.StatusBlock != "" {
		fmt.Fprintf(&b, "%s\n", ctx.StatusBlock)
	}

	b.WriteString(`## Your Task: Planning Phase

Create a detailed implementation plan. Every acceptance criterion must map to specific implementation tasks.

### Requirements
1. Each task must specify: files to modify, what changes, why this approach
2. Each task must include verification items (testable assertions)
3. Tasks must be ordered by dependency (what must come first)
4. Tasks must not overlap in file scope (for parallel execution)
5. Every acceptance criterion must be covered by at least one task
6. Include a "ship blockers" section — what absolutely must work

### Output Format
Produce a JSON plan:
` + "```json" + `
{
  "tasks": [
    {
      "id": "t-1",
      "description": "...",
      "files": ["path/to/file.go"],
      "changes": "...",
      "rationale": "...",
      "depends_on": [],
      "criteria_covered": ["c-1"],
      "verification": [
        "GET /api/auth returns 200 with valid JWT",
        "GET /api/auth returns 401 without token"
      ]
    }
  ],
  "ship_blockers": [
    "All tests pass",
    "No TODO comments in committed code"
  ]
}
` + "```" + `

`)

	if ctx.ResearchBlock != "" {
		fmt.Fprintf(&b, "## Research Findings\n%s\n", ctx.ResearchBlock)
	}
	if ctx.GapsBlock != "" {
		fmt.Fprintf(&b, "## Known Gaps (from prior validation)\n%s\n", ctx.GapsBlock)
	}
	if ctx.HandoffBlock != "" {
		fmt.Fprintf(&b, "## Handoff Context\n%s\n", ctx.HandoffBlock)
	}

	return b.String()
}

// BuildMissionExecutePrompt generates the prompt for the Executing phase.
// The agent must implement the plan and satisfy acceptance criteria.
func BuildMissionExecutePrompt(ctx MissionContext, taskDescription string, verification []string) string {
	var b strings.Builder

	fmt.Fprintf(&b, `You are an implementation agent working on mission "%s".

## Mission
**Title:** %s
**Intent:** %s

`, ctx.MissionID, ctx.Title, ctx.Intent)

	if ctx.CriteriaBlock != "" {
		fmt.Fprintf(&b, "%s\n", ctx.CriteriaBlock)
	}
	if ctx.StatusBlock != "" {
		fmt.Fprintf(&b, "%s\n", ctx.StatusBlock)
	}

	if ctx.GapsBlock != "" {
		fmt.Fprintf(&b, "%s\n", ctx.GapsBlock)
	}

	fmt.Fprintf(&b, "## Your Task\n%s\n\n", taskDescription)

	if len(verification) > 0 {
		b.WriteString("## Verification Requirements\nYour work MUST satisfy these:\n")
		for _, v := range verification {
			fmt.Fprintf(&b, "- [ ] %s\n", v)
		}
		b.WriteString("\n")
	}

	b.WriteString(`## Execution Philosophy

### Maximum Complete Effort
There is no "good enough." If a better pattern exists, refactor to it. If a security
hardening step is possible, do it. If a test can be added, add it. The only question is:
"Is there MORE that can be done to make this correct, complete, and production-grade?"

### Scope Expansion, Not Scope Defense
If you discover adjacent broken or incomplete work during implementation, FIX IT.
The user's intent is a working SYSTEM, not a working function within a broken system.
Pre-existing issues are YOUR problem. Scaffolded code must be completed. Stubs must
be implemented. TODOs must be resolved.

### No Excuses, No Deferrals
Do NOT generate reasons to stop working:
- "This is a pre-existing issue" → Fix it anyway.
- "This would require too much refactoring" → Do the refactoring.
- "This is out of scope" → If it affects the system, it's in scope.
- "This is blocked by X" → Unblock it. Build X.
- "I've completed the requested changes" → Is the SYSTEM complete?

## Rules
1. Implement FULLY. No stubs, no TODOs, no "implement later" comments.
2. Write ADVERSARIAL tests — tests that try to BREAK the code, not just prove it compiles.
   Test edge cases, error paths, boundary conditions, concurrent access, malformed input.
3. Follow existing codebase patterns and conventions.
4. Do NOT run git add, git commit, or git push — Stoke handles that.
5. Run build and test commands to verify your work before declaring done.
6. If you discover a blocker, RESOLVE IT. Only flag truly external, unreachable constraints.
7. Every error must be wrapped with context. No bare "return err".
8. Every public function needs a test. Every error path needs a test.
9. Verify your work compiles, tests pass, and lint is clean BEFORE declaring done.

## Codebase Tools
You have MCP tools for understanding the codebase while implementing:
- **search_symbols**: Find existing functions/types to reuse or extend
- **get_dependencies**: See what a file imports and what depends on it
- **search_content**: Find related code by concept
- **get_file_symbols**: See what a file exports before modifying it
- **impact_analysis**: Check what will break if you change a file
- **find_symbol_usages**: Find all consumers of a function/type before changing its signature
- **trace_entry_points**: See which surfaces (CLI, API, Web) reach the code you're changing
- **semantic_search**: Find conceptually related code by meaning, not keywords

Use these BEFORE writing code to understand existing patterns.
Use them AFTER writing code to verify you haven't broken consumers.

## Output Format
When you are done, list every file you created or modified:
FILE: <path>

This tells the harness what changed so it can validate your work.
`)

	if ctx.HasFrontend {
		b.WriteString(`## UX Quality Rules (REQUIRED — this is a frontend project)
These are NOT optional. The convergence validator will reject your work if these are violated:
- Every image MUST have alt text. Every form input MUST have a label.
- Every interactive element MUST be keyboard-accessible (use semantic HTML: button, a, input).
- Layout MUST be responsive — test at mobile (320px), tablet (768px), and desktop widths.
- Every async operation MUST have loading AND error states — no blank screens.
- Use CSS variables or design tokens for colors — no hardcoded hex in inline styles.
- Handle empty states explicitly — show helpful messages when there's no data.
- Focus indicators must be visible for keyboard navigation.
`)
		if ctx.UIFramework == "react" {
			b.WriteString(`- Wrap app root in ErrorBoundary with user-friendly fallback UI.
- Use key props on every list item rendered via .map().
- No dangerouslySetInnerHTML without DOMPurify sanitization.
`)
		}
		if ctx.HasStorybook {
			b.WriteString("- Every new component MUST have a Storybook story covering its variants.\n")
		}
		if ctx.TestFramework != "" {
			fmt.Fprintf(&b, "- Write component tests using %s — test user interactions, not implementation details.\n", ctx.TestFramework)
		}
		b.WriteString("\n")
	}

	if ctx.GapsBlock != "" {
		fmt.Fprintf(&b, "## Open Gaps (must address)\n%s\n", ctx.GapsBlock)
	}
	if ctx.ResearchBlock != "" {
		fmt.Fprintf(&b, "## Research Context\n%s\n", ctx.ResearchBlock)
	}
	if ctx.HandoffBlock != "" {
		fmt.Fprintf(&b, "## Prior Agent Context\n%s\n", ctx.HandoffBlock)
	}
	if ctx.PriorContext != "" {
		fmt.Fprintf(&b, "## Additional Context\n%s\n", ctx.PriorContext)
	}

	return b.String()
}

// BuildMissionValidatePrompt generates the prompt for the Validating phase.
// The agent must adversarially check whether the work is actually complete.
func BuildMissionValidatePrompt(ctx MissionContext) string {
	var b strings.Builder

	fmt.Fprintf(&b, `You are a validation agent reviewing mission "%s".

## Mission
**Title:** %s
**Intent:** %s

`, ctx.MissionID, ctx.Title, ctx.Intent)

	if ctx.CriteriaBlock != "" {
		fmt.Fprintf(&b, "%s\n", ctx.CriteriaBlock)
	}
	if ctx.StatusBlock != "" {
		fmt.Fprintf(&b, "%s\n", ctx.StatusBlock)
	}

	b.WriteString(`## Codebase Tools

You have tools to read and search the codebase. USE THEM — do not guess.
- **Read**: Read any file by path to verify implementation details.
- **Glob**: Find files by pattern (e.g. "**/*_test.go") to check coverage.
- **Grep**: Search file content for patterns (e.g. find all callers of a function).
- **Bash**: Run build/test/lint commands to verify the system actually works.

For every claim of completion, READ THE ACTUAL CODE. "Looks right" is not evidence.

## Your Task: Adversarial Validation

You are NOT here to confirm the work is good. You are here to find reasons it ISN'T done.
Your default assumption is: THE WORK IS NOT DONE. Prove yourself wrong with evidence.

### Evidence-Based Done Claims
Any claim that work is complete MUST cite specific evidence:
- "Tests pass" → Which tests? What coverage? What paths are untested?
- "API works" → Show request/response. Show error handling. Show auth.
- "UI is responsive" → Show test results at 320px, 768px, 1024px, 1440px.
Vague affirmations ("looks good," "should work," "appears complete") are AUTOMATICALLY REJECTED.

### The 5 Convergence Gates (ALL must be TRUE)

**Gate 1: User intent is fully satisfied**
For each acceptance criterion, find PROOF it's met. Not "it looks right" — show the
test, show the assertion, show the output. No proof = not met.

**Gate 2: Everything works — the ENTIRE system, not just what was changed**
Build passes. ALL tests pass. Lint passes. Not just the tests for the changed code —
ALL tests in the entire repository. Pre-existing failures are YOUR problem too.

**Gate 3: Engineering standards are exhaustive**

*Security (non-negotiable):*
- No SQL injection, command injection, path traversal, XSS, hardcoded secrets
- No hardcoded credentials, API keys, tokens — use environment variables or vault
- Every HTTP endpoint has authentication and authorization checks (RBAC)
- Input validation on ALL external boundaries (user input, API params, file uploads)
- Output encoding: context-appropriate for HTML, SQL, shell, URL, JSON
- CORS policies explicit and minimal (no Access-Control-Allow-Origin: * in production)
- CSP headers configured (no unsafe-inline, no unsafe-eval without justification)
- Rate limiting on public endpoints with proper 429 responses
- CSRF protection on state-changing operations
- Security headers: HSTS, X-Content-Type-Options, X-Frame-Options
- Dependencies pinned to exact versions, scanned for known CVEs
- Audit trail: sensitive operations logged with actor, action, target, timestamp

*Code quality:*
- No TODOs, FIXMEs, HACKs, stubs, placeholders, or "implement later" anywhere
- No panics in production code. No ignored errors (no _ = fn()). No bare any/interface{}.
- No type safety bypasses (@ts-ignore, nolint, etc.)
- No debug print statements — use structured logging with levels
- No duplicate logic (DRY) — same code in 3+ places must be abstracted
- Every error wrapped with context: fmt.Errorf("doing X: %%w", err) not bare return err
- Consistent naming conventions across the entire codebase
- Immutability by default — mutable state only where justified
- Typed error hierarchies — no throw new Error("something broke")
- Dead code elimination — no unused imports, unreachable branches, orphaned files

*Testing (adversarial, not confirmatory):*
- Tests verify BEHAVIOR against the CONTRACT, not the implementation
- Every function returning an error has a test exercising the error path
- Edge cases: nil inputs, empty strings, concurrent access, boundary values, malformed data
- Integration tests verify cross-component wiring end-to-end
- Contract tests verify API boundaries between services
- Negative tests: invalid input, missing permissions, network failures, timeout conditions
- No mocks where real implementations are feasible (test containers, in-memory DBs preferred)
- No flaky tests: no time-dependent without controlled clocks, no order-dependent
- Test names describe the scenario being tested, not the function name

*Reliability & Resilience:*
- Idempotency on write operations (safe to retry)
- Circuit breakers on external service calls
- Retry with exponential backoff and jitter on retryable operations
- Graceful degradation when dependencies fail
- Graceful shutdown: in-flight requests completed, connections drained
- Timeouts on EVERY network call, database query, and file operation
- Connection pooling configured and leak detection enabled
- Concurrency safety: no race conditions, no deadlocks, proper locking
- Transaction boundaries: correct isolation, no partial commits

*Observability:*
- Structured logging: JSON with timestamp, level, service, trace_id, operation, duration
- Distributed tracing: trace context propagated across service boundaries
- Metrics: RED (rate/errors/duration) on endpoints, USE (utilization/saturation/errors) on resources
- Error tracking: centralized, stack traces preserved, deduplication

*Architecture:*
- Single Responsibility: functions do ONE thing
- Dependency Inversion: public interfaces stable, implementations swappable
- No God objects — decompose types with too many methods/fields
- Concurrent operations safe (mutexes, channels, atomics)
- Design patterns applied where appropriate (repository, factory, strategy, circuit breaker)

*Database & Data:*
- Migrations: forward and rollback tested
- Indexing: every query path has appropriate indexes, no full table scans
- No N+1 query patterns
- Schema matches application models exactly
- TTL/retention policies: no unbounded data growth

*API Design:*
- Full documentation (OpenAPI/Swagger for REST, proto for gRPC)
- Consistent error responses: machine-readable codes, human-readable messages, field-level detail
- Cursor-based pagination on list endpoints (no offset on large datasets)
- Idempotency-Key support on mutating endpoints
- Proper status codes (not everything is 200 or 500)

`)

	if ctx.HasFrontend {
		b.WriteString(`**Gate 3a: UX quality — THIS IS A FRONTEND PROJECT, these are mandatory**
- ALL images have alt attributes (WCAG 2.1 Level A)
- ALL form inputs have associated labels or aria-label
- ALL interactive elements are keyboard-accessible (no onClick on div without role/tabIndex)
- Focus indicators are never removed without replacement (outline:none requires :focus-visible)
- Responsive viewport meta tag present in HTML documents
- Stylesheets use media queries and responsive units — layout works on mobile/tablet/desktop
- Data-fetching components have loading states AND error states — no blank screens
- No dangerouslySetInnerHTML without sanitization (XSS vector)
- List rendering uses key props (React .map() without key = render bugs)
- No hardcoded colors in inline styles — use CSS variables or design tokens for theming
- UI is consistent: spacing, typography, color palette follow a design system
- Forms have client-side validation with clear error messages
- Empty states are handled — don't show blank pages when there's no data
`)
		if ctx.UIFramework == "react" {
			b.WriteString("- React apps have ErrorBoundary at the root — render errors show fallback, not blank page\n")
		}
		if ctx.HasStorybook {
			b.WriteString("- Every component has Storybook stories covering its variants\n")
		}
	}

	b.WriteString(`

**Gate 4: Everything was researched and confirmed accurate**
No guesses. No hallucinated APIs. Every external dependency, every algorithm choice,
every security decision must be based on verified knowledge.

**Gate 5: Everything in the repo that was started is finished**
Look at the ENTIRE repository, not just the files that were changed. If there are
scaffolded features, half-built modules, or partially implemented code ANYWHERE in
the repo, those are failures. If someone started something, it must be finished.

### IMPORTANT: Do not rationalize
Models will find reasons to say "done" — architecture decisions, pre-existing issues,
"out of scope" — ignore those impulses. If something can be fixed or improved, it's
not done. The question is not "is this acceptable?" The question is "is there ANYTHING
else that could be done to make this better?" If yes, it's incomplete.

### CRITICAL: "Exists" is not "Works"
The most dangerous failure mode is declaring something done because the CODE exists
without verifying it's WIRED, INVOKED, and TESTED end-to-end. Specifically check:
- New types/functions declared → are they actually called from the integration layer?
- New config fields added → does any code read them and change behavior?
- New rules/checks added → are they registered in the default set AND run during validation?
- New parameters accepted → do callers actually pass meaningful values?
- New conditional logic → do tests exercise BOTH the true AND false branches?
Dead code that looks complete is WORSE than missing code because it creates false confidence.

### Output Format
Return JSON:
` + "```json" + `
{
  "verdict": "complete" | "incomplete",
  "score": 0.0-1.0,
  "criteria_status": [
    {"id": "c-1", "satisfied": true, "evidence": "jwt_test.go:42 verifies token generation"},
    {"id": "c-2", "satisfied": false, "reason": "no test for invalid token rejection"}
  ],
  "gaps": [
    {
      "category": "test|code|security|completeness|optimization|consistency",
      "severity": "blocking",
      "file": "path/to/file.go",
      "line": 42,
      "description": "...",
      "suggestion": "..."
    }
  ],
  "reasoning": "Overall assessment — be specific about what's missing"
}
` + "```" + `

### Rules
- Do NOT modify any files. Read only.
- Do NOT run git commands.
- Be specific: file paths, line numbers, concrete evidence.
- "Looks good" is NOT evidence. Show the test, show the assertion, show the output.
- When in doubt, flag it. False positives are better than missed issues.
- ALL gap severities should be "blocking" — there is no "nice to have."

`)

	if ctx.GapsBlock != "" {
		fmt.Fprintf(&b, "## Previously Identified Gaps\nVerify these are resolved:\n%s\n", ctx.GapsBlock)
	}
	if ctx.PriorContext != "" {
		fmt.Fprintf(&b, "%s\n", ctx.PriorContext)
	}

	return b.String()
}

// BuildMissionConsensusPrompt generates the prompt for the consensus check.
